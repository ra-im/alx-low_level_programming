#include "3-calc.h"

/**
 * op_add - adds two integers.
 *
 * @a: integer 1.
 * @b: integer 2.
 *
 * Return: sum of a and b.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - determines the differene betweeen two integers.
 *
 * @a: interger 1.
 * @b: integer 2.
 *
 * Return: difference of a and b.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - determines the product tof two integers.
 *
 * @a: integer 1.
 * @b: integer 2.
 *
 * Return: product of a and b.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - determines the result of the division
 *		of two integers.
 *
 * @a: integer 1 (numerator).
 * @b: integer 2 (denominator).
 *
 * Return: result of te division of a and b.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Determines the modulo of two integers.
 *
 * @a: integer 1 (numerator).
 * @b: integer 2 (denominator).
 *
 * Return: remainder of a divided by b.
 */

int op_div(int a, int b)
{
	return (a % b);
}

